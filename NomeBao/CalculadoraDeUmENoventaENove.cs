namespace NomeBao
{
    public class CalculadoraDeUmENoventaENove
    {
        public static int Somar(int n, int N)
        {
            return n + N;
        }
        public static int Subtrair(int n, int N)
        {
            return n - N;
        }
        public static int Dividir(int n, int N)
        {
            return n / N;
        }
        public static int Multiplicar(int n, int N)
        {
            return n * N;
        }
    }
}
